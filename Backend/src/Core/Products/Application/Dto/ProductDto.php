<?php

namespace App\Core\Products\Application\Dto;


class ProductDto
{
    public int $id;
    public string $uuid;
    public string $name;
    public string $description;
    public string $image;
    public float $price;
    public int $category;
    public string $barcode;
    public int $unitOfMeasurement;
    public int $featuredProduct;
    public float $cost;

    /**
     * @return int
     */
    public function getId(): int
    {
        return $this->id;
    }

    /**
     * @param int $id
     */
    public function setId(int $id): void
    {
        $this->id = $id;
    }

    /**
     * @return string
     */
    public function getUuid(): string
    {
        return $this->uuid;
    }

    /**
     * @param string $uuid
     */
    public function setUuid(string $uuid): void
    {
        $this->uuid = $uuid;
    }

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }

    /**
     * @param string $name
     */
    public function setName(string $name): void
    {
        $this->name = $name;
    }

    /**
     * @return string
     */
    public function getDescription(): string
    {
        return $this->description;
    }

    /**
     * @param string $description
     */
    public function setDescription(string $description): void
    {
        $this->description = $description;
    }

    /**
     * @return string
     */
    public function getImage(): string
    {
        return $this->image;
    }

    /**
     * @param string $image
     */
    public function setImage(string $image): void
    {
        $this->image = $image;
    }

    /**
     * @return float
     */
    public function getPrice(): float
    {
        return $this->price;
    }

    /**
     * @param float $price
     */
    public function setPrice(float $price): void
    {
        $this->price = $price;
    }

    /**
     * @return int
     */
    public function getCategory(): int
    {
        return $this->category;
    }

    /**
     * @param int $category
     */
    public function setCategory(int $category): void
    {
        $this->category = $category;
    }

    /**
     * @return string
     */
    public function getBarcode(): string
    {
        return $this->barcode;
    }

    /**
     * @param string $barcode
     */
    public function setBarcode(string $barcode): void
    {
        $this->barcode = $barcode;
    }

    /**
     * @return int
     */
    public function getUnitOfMeasurement(): int
    {
        return $this->unitOfMeasurement;
    }

    /**
     * @param int $unitOfMeasurement
     */
    public function setUnitOfMeasurement(int $unitOfMeasurement): void
    {
        $this->unitOfMeasurement = $unitOfMeasurement;
    }

    /**
     * @return int
     */
    public function getFeaturedProduct(): int
    {
        return $this->featuredProduct;
    }

    /**
     * @param int $featuredProduct
     */
    public function setFeaturedProduct(int $featuredProduct): void
    {
        $this->featuredProduct = $featuredProduct;
    }

    /**
     * @return float
     */
    public function getCost(): float
    {
        return $this->cost;
    }

    /**
     * @param float $cost
     */
    public function setCost(float $cost): void
    {
        $this->cost = $cost;
    }

}